const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const userValidation = require('../../validations/user.validation');
const userController = require('../../controllers/user.controller');
const { multerUpload } = require('../../config/aws.config');
const router = express.Router();

router
  .route('/profile')
  .get(auth('manageProfile'), userController.getMyProfile)
  .post(auth('manageProfile'), validate(userValidation.updateProfile), userController.updateMyProfile);

router
  .route('/profile/pic')
  .patch(auth('manageProfile'), multerUpload.single('profilePic'), userController.updateUserProfilePic);

router
  .route('/')
  .post(auth('manageUsers'), validate(userValidation.createUser), userController.createUser)
  .get(auth('getUsers'), validate(userValidation.getUsers), userController.getUsers);

router.route('/available').get(auth('getAvailableUsers'), userController.getAvailableUsers);

router
  .route('/invite')
  .get(auth('manageProfile'), userController.getMyInvites)
  .post(auth('manageProfile'), validate(userValidation.inviteUser), userController.inviteUser);

router.route('/invite/count').get(auth('manageProfile'), userController.getMyInvitesCount);

router
  .route('/invite/accept/:accepted/:inviteId')
  .post(auth('manageProfile'), validate(userValidation.acceptInvite), userController.acceptInvite);

router
  .route('/invite/accept-all/:accepted')
  .post(auth('manageProfile'), validate(userValidation.acceptAllInvites), userController.acceptAllInvites);

router.route('/connections').get(auth('manageProfile'), userController.getMyConnections);
router.route('/connection/:connectionId').delete(auth('manageProfile'), userController.deleteMyConnection);
router.route('/connections/count').get(auth('manageProfile'), userController.getMyConnectionsCount);

router
  .route('/:userId')
  .get(auth('getUsers'), validate(userValidation.getUser), userController.getUser)
  .patch(auth('manageUsers'), validate(userValidation.updateUser), userController.updateUser)
  .delete(auth('manageUsers'), validate(userValidation.deleteUser), userController.deleteUser);

module.exports = router;

